    private boolean show = true;

    public GD_DangNhap(String title) {
        super(title);
        initComponents();
        setBackground(new Color(0, 0, 0, 0));
        setOpacity(0);
        TimingTarget target = new TimingTargetAdapter() {
            @Override
            public void timingEvent(float fraction) {
                if (show) {
                    setOpacity(fraction);
                } else {
                    setOpacity(1f - fraction);
                }
            }

            @Override
            public void end() {
                if (!show) {
                    System.exit(0);
                }
            }
        };
        Animator animator = new Animator(300, target);
        animator.setResolution(0);
        animator.setAcceleration(0.5f);
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowOpened(WindowEvent e) {
                if (!animator.isRunning()) {
                    animator.start();
                }
            }
        });
        login();
    }

    private void login() {
        pnlForm.login(new EventOnClick() {
            @Override
            public void onClick(Object object) {
                NhanVien nhanVien = (NhanVien) object;
                if (nhanVien != null) {
                    GD_DangNhap.this.dispose();
                    new GD_Chinh("Quản lý Karaoke FourH", nhanVien).setVisible(true);
                } else {
                    pnlForm.showMessage(Message.MessageType.ERROR, "Sai mật khẩu");
                }
            }
        });
    }

